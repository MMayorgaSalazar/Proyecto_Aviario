    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        if (!"".equals(Clase.getSelectedItem()) || !"".equals(Nro_Ave.getText()) || !"".equals(Nacimiento.getText()) || !"".equals(Especie.getText()) || !"".equals(Jaula.getText())){
            a.setNro(Nro_Ave.getText());
            a.setF_nacimiento(Nacimiento.getText());
            a.setEspecie(Especie.getText());
            a.setJaula(Integer.parseInt(Jaula.getText()));
            a.setClase(Clase.getSelectedItem().toString());
            am.RegistrarAve(a);
            JOptionPane.showMessageDialog(null, "Ave registrada exitosamente");
            this.dispose();
        }else {
            JOptionPane.showMessageDialog(null, "Los campos estan vacias ");
        }
        
    }//GEN-LAST:event_GuardarActionPerformed

